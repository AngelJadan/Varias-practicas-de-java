    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        Persona personaIng=datos();
        boolean res=cpersona.insertar(personaIng);
        if (res==true) {
            JOptionPane.showMessageDialog(this, "Datos guardados exitosamente");
        }else{
            JOptionPane.showMessageDialog(this, "No se ha podido guardar los datos");
        }
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Persona pbuscado=cpersona.buscar(Integer.parseInt(txtcodigo.getText()));
        String codigo=String.valueOf(pbuscado.getCodigo());
        String nombre=pbuscado.getNombre();
        String direccion=pbuscado.getDireccion();
        String fecha=pbuscado.getFechaCreacion();
        String numero=String.valueOf(pbuscado.getNumeroEstuantes());
        
        txtcodigo.setText(codigo);
        txtnombre.setText(nombre);
        txtdireccion.setText(direccion);
        txtfecha.setText(fecha);
        txtnumero.setText(numero);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        // TODO add your handling code here:
        
        Persona p=datos();
        boolean res=cpersona.actulizar(p);
        if (res==true) {
            JOptionPane.showMessageDialog(this,"Datos Actulizados");
            txtcodigo.setText("");
            txtnombre.setText("");
            txtdireccion.setText("");
            txtfecha.setText("");
            txtnumero.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Se ha generado un error");
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

