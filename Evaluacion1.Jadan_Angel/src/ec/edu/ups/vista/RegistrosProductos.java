    /**
     * Este metodo sirve para imprimir la lista de productos y sumar el costo para el total sel inventario
     */
    public void inventario() {
        List<Producto> lista = cproducto.listadoLectura();
        double inventario=0;
        txtalista.setText(" Codigo Nombre  Stock Costo\n");
        for (Producto producto : lista) {
            txtalista.append(producto.getCodigo() + "      " + producto.getNombre() + "      " + producto.getStock() + "    " + producto.getCosto() + "\n");
            inventario=inventario+producto.getCosto();
        }
        cproducto.costoInventario(String.valueOf(inventario));
        txttotal.setText(String.valueOf(inventario));

    }
