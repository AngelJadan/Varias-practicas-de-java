    /**
     * Este metodo sirve para enviar al controlador los datos capturados
     */
    public void guardar() {
        Producto p = new Producto();
        int codigo = Integer.parseInt(txtcodigo.getText());
        String nombre = txtnombre.getText();
        int stock = Integer.parseInt(txtstock.getText());
        Double costo = Double.parseDouble(txtctotal.getText());
        p.setCodigo(codigo);
        p.setNombre(nombre);
        p.setStock(stock);
        p.setCosto(costo);

        boolean res = cproducto.crear(p);
        if (res == true) {
            JOptionPane.showMessageDialog(this, "Producto registrado");
            txtcodigo.setText("");
            txtnombre.setText("");
            txtstock.setText("");
            txtcosto.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se ha podido registrar");
        }

    }
