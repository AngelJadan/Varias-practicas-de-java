    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:

        Persona per=new Persona();
        ControladorPersona controlador=new ControladorPersona();

        per = new Persona();

        if (txtCodigo.equals("null") || txtCedula.equals(null) || txtNombre.equals(null) || txtApellido.equals(null) || txtTelefono.equals(null) || txtaDireccion.equals(null)) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
        } else {
            per.setCodigo(txtCodigo.getText());
            per.setCedula(txtCedula.getText());
            per.setNombre(txtNombre.getText());
            per.setApellido(txtApellido.getText());

            if (rbGeneroF.isSelected()) {
                per.setGenero("F");
            } else {
                per.setGenero("M");
            }

            int edad = (Integer) cbxEdad.getSelectedItem();
            per.setEdad(edad);

            per.setTelefono(txtTelefono.getText());
            per.setDireccion(txtaDireccion.getText());

            controlador.crear(per);

            JOptionPane.showMessageDialog(null, "Persona guardado con exito");

        }
    }//GEN-LAST:event_btnAceptarActionPerformed

