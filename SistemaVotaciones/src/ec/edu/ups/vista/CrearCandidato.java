    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        Candidato candidato = new Candidato();
        candidato.setCodigo(Integer.parseInt(txtCodigo.getText()));

        candidato.setCedula(txtCedula.getText());
        candidato.setNombre(txtNombre.getText());

        candidato.setApellido(txtApellido.getText());

        String edad = (String) cbxEdad.getSelectedItem();
        candidato.setEdad(Integer.parseInt(edad));
        candidato.setUrlImagen((ImageIcon) lblImagen.getIcon());
        candidato.setPartido(txtpartido.getText());
        candidato.setCargo(txtcargo.getText());

        if (jrbMasculino.isSelected()) {
            candidato.setGenero(jrbMasculino.getText());
        } else {
            candidato.setGenero(jrbFemenino.getText());
        }
        ccandidato.crear(candidato);
        JOptionPane.showMessageDialog(this, "CANDIDATO CREADO ");

        txtCodigo.getText();
        txtCedula.setText("");
        txtNombre.setText("");
        int v=Integer.valueOf(txtCodigo.getText());
        v++;
        txtCodigo.setText(String.valueOf(v));
    }//GEN-LAST:event_btnGuardarActionPerformed

