    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        
        DefaultTableModel modelo = (DefaultTableModel) tblvotante.getModel();//ASIGNAMOS EL MODELO QUE TIENE NUESTRO JTABLE
        tblvotante.setRowHeight(20);

        Object[] vot = new Object[13];

        int filas = modelo.getRowCount();//DECUELVE LA CANTIDAD  DE FILAS QUE HAY EN LA TABLA, SE DECLARA 
        for (int i = filas - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
        for (Votante votante : cvotante.getlVotante()) {

            vot[0] = votante.getCodigo();
            vot[1] = votante.getCedula();
            vot[2] = votante.getNombre();
            vot[3] = votante.getApellido();
            vot[4] = votante.getEdad();
            vot[5] = votante.getGenero();
            vot[6] = votante.getTelefono();
            vot[7]=votante.getDireccion();
            vot[8]=votante.getProvincia();
            vot[9]=votante.getCanton();
            vot[10]=votante.getParroquia();
            vot[11]=votante.getRecinto();
            vot[12]=votante.getUrlImagen();
            
            modelo.addRow(vot);//AGREGAMOS LA FILA A LA LISTA

        }
        tblvotante.setModel(modelo);
    }//GEN-LAST:event_btnactualizarActionPerformed

