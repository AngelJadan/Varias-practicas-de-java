    private void btnaccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaccederActionPerformed
        // TODO add your handling code here:
        Votante votante=new Votante();
        
        for (Votante votante1 : cvotante.getlVotante()) {
            if (votante1.getCedula()==txtcedula.getText()) {
                txtnombre.setText(votante.getNombre());
                txtapellido.setText(votante.getApellido());
                txtedad.setText(String.valueOf(votante.getEdad()));
            }else{
                JOptionPane.showMessageDialog(this, "Persona no encontrada");
            }
        }
        
    }//GEN-LAST:event_btnaccederActionPerformed

