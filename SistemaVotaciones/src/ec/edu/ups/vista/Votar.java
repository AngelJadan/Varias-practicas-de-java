    private void btnsiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsiguienteActionPerformed
        // TODO add your handling code here:
        
        Candidato cantidato=new Candidato();
        contador++;
        ccandidato.buscar(contador);
        txtnombrecandidato.setText(cantidato.getNombre());
        txtcapellido.setText(cantidato.getApellido());
        txtpartido.setText(cantidato.getPartido());
        lblfoto.setDisabledIcon(cantidato.getUrlImagen());
        
        
    }//GEN-LAST:event_btnsiguienteActionPerformed

    private void btnvotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvotarActionPerformed
        // TODO add your handling code here:
        
        Voto voto=new Voto();
        voto.setCodigo(Integer.parseInt(txtcodigovoto.getText()));
        Candidato candidato=new Candidato();
        candidato.setNombre(txtnombrecandidato.getText());
        candidato.setApellido(txtcapellido.getText());
        candidato.setPartido(txtpartido.getText());
        voto.setCandidato(candidato);
        
        Votante votante=new Votante();
        votante.setCedula(txtcedula.getText());
        votante.setNombre(txtnombre.getText());
        votante.setEdad(Integer.parseInt(String.valueOf(txtedad)));
        voto.setVotante(votante);
        
        cvoto.crear(WIDTH, voto);
        JOptionPane.showMessageDialog(this,"Su voto ha sido procesdo");
                
    }//GEN-LAST:event_btnvotarActionPerformed

