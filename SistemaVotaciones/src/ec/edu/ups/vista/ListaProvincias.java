    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
                
        
        
        DefaultTableModel modelo = (DefaultTableModel) tblprovincias.getModel();//ASIGNAMOS EL MODELO QUE TIENE NUESTRO JTABLE
        tblprovincias.setRowHeight(20); 
        
        Object [] prov=new Object[2];
        
        //METODO PARA LIMPIAR   LA TABLA
       int filas= modelo.getRowCount();//DECUELVE LA CANTIDAD  DE FILAS QUE HAY EN LA TABLA, SE DECLARA 
        for (int i = filas - 1; i >= 0; i--) {
              modelo.removeRow(i);
        }
        for ( Provincia provincia : cprovincia.getLprov()) {
            
            prov[0] = provincia.getCodigo();
            prov[1] = provincia.getNombre();
            modelo.addRow(prov);//AGREGAMOS LA FILA A LA LISTA
       
        }
        tblprovincias.setModel(modelo);
     
        
    }//GEN-LAST:event_btnactualizarActionPerformed

