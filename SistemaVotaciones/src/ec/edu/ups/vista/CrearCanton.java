    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Canton canton = new Canton();
        canton.setCodigo(Integer.parseInt(txtcodigo.getText()));
        canton.setCanton(txtnombre.getText());
        canton.setProvincia((Provincia)cboxProvincia.getSelectedItem());
        ccanton.crear(canton);
        JOptionPane.showMessageDialog(this, "Canton Creado");
        txtcodigo.setText(String.valueOf(canton.getCodigo() + 1));
    }//GEN-LAST:event_jButton1ActionPerformed

