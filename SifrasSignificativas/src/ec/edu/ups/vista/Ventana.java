    public void recorrido() {

        int c = 0;
        char aux=0;
        try {

            double num = Double.parseDouble(txtnumero.getText());
            String n = txtnumero.getText();

            for (int i = 0; i < n.length(); i++) {
                aux = n.charAt(i);
                if (aux != '.') {
                    if (aux == '0' && c > 0) {
                        c++;
                    }
                    if (aux != '0') {
                        c++;
                    }
                }
                txtsifras.setText(String.valueOf(c));
            }

        } catch (Exception e) {

        }
    }
