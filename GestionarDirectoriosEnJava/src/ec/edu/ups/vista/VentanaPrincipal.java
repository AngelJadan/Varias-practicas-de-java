    private void btndirectorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndirectorioActionPerformed
        // TODO add your handling code here:
        cdir.setRuta(txtruta.getText());
        List<String> lista = cdir.listarDirectorios();

        DefaultListModel mlista = new DefaultListModel();
        for (String direc : lista) {
            mlista.addElement(direc);
        }
        jllista.setModel(mlista);

    }//GEN-LAST:event_btndirectorioActionPerformed

