package ec.edu.ups.modelo;

/**
 *
 * @author Angel
 */
public class Proveedor extends Persona{
    private int codigo;
    private Persona persona;
    private String ncomercial;
    
    public Proveedor(){
        
    }

    public Proveedor(int codigo, Persona persona, String ncomercial) {
        this.codigo = codigo;
        this.persona = persona;
        this.ncomercial = ncomercial;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public Persona getPersona() {
        return persona;
    }

    public void setPersona(Persona persona) {
        this.persona = persona;
    }

    public String getNcomercial() {
        return ncomercial;
    }

    public void setNcomercial(String ncomercial) {
        this.ncomercial = ncomercial;
    }
    public Identificacion getIdentificacion() {
        return identificacion;
    }

    public void setIdentificacion(Identificacion identificacion) {
        this.identificacion = identificacion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    @Override
    public String toString() {
        return "Proveedor{" + "codigo=" + codigo + ", persona=" + persona + ", ncomercial=" + ncomercial + '}';
    }
    
}
