    public void guardarTrabajador() {
        String tipoId = cboxtiipoid.getSelectedItem().toString();
        String identificacion = txtidentificacion.getText();
        String nombre = txtnombre.getText();
        String apellido = txtapellido.getText();
        String fNacimiento = date.getText();
        String telefono = txttelefono.getText();
        String correo = txtcorreo.getText();
        String direccion = txtdireccion.getText();
        
        trabajador.setTipoIdentificacion(tipoId);
        trabajador.setIdentificacion(identificacion);
        trabajador.setNombre(nombre);
        trabajador.setAplellido(apellido);
        trabajador.setFechaDeNacimiento(fNacimiento);
        trabajador.setTelefono(telefono);
        trabajador.setCorreoElectronico(correo);
        trabajador.setDireccion(direccion);
        
        boolean validado = validarCampos();
        if (validado == true) {
            boolean estado = ctrabaTrabajador.crearTrabajador(trabajador, empresa.getCodigo());
            if (estado == true) {
                JOptionPane.showMessageDialog(this, "Los el trabajador ha sido guardado");
                deshabTextos();
            } else {
                JOptionPane.showMessageDialog(this, "No se ha podido guardar el trabajador, revise por favor");
            }
        }
    }
    
