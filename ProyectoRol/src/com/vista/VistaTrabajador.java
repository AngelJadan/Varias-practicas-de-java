    public boolean validarCampos() {
        boolean estado = false;
        if (txtidentificacion.getText() == null
                || txtnombre.getText() == null || txtapellido.getText() == null
                || txttelefono.getText() == null || txtcorreo.getText() == null
                || txtdireccion.getText() == null) {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos");
            estado = false;
        } else {
            estado = true;
        }
        return estado;
    }
    
    public void buscarTrabajador() {
        String id = txtidentificacion.getText();
        trabajador = ctrabaTrabajador.buscarTrabajador(id, "", empresa.getCodigo());
        
        if (trabajador.getIdentificacion() != null) {
            cboxtiipoid.setSelectedItem(trabajador.getTipoIdentificacion());
            txtnombre.setText(trabajador.getNombre());
            txtapellido.setText(trabajador.getAplellido());
            date.setText(trabajador.getFechaDeNacimiento());
            txtcorreo.setText(trabajador.getCorreoElectronico());
            txttelefono.setText(trabajador.getTelefono());
            txtdireccion.setText(trabajador.getDireccion());
            deshabTextos();
            btneliminar.setVisible(true);
            btnacargas.setVisible(true);
            btngencontrato.setVisible(true);
        } else if (trabajador.getIdentificacion() == null) {
            JOptionPane.showMessageDialog(this, "El trabajador no se ha encontrado");
            btneditar.setVisible(false);
            btneliminar.setVisible(false);
            btnacargas.setVisible(false);
            btngencontrato.setVisible(false);
        }
    }
    
