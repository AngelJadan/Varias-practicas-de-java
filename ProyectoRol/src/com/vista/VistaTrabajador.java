    private void txtidentificacionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidentificacionKeyReleased
        // TODO add your handling code here:

        try {
            if (evt.getKeyCode() != 10) {
                String cedula = txtidentificacion.getText();
                String tipoCedula = cboxtiipoid.getSelectedItem().toString();
                if (cedula.length() > 10 && tipoCedula == "Cedula") {
                    JOptionPane.showMessageDialog(this, "El numero de cedula no debe tener mas de 10 caracteres");
                } else {
                    if (cedula.length() == 10 && tipoCedula == "Cedula") {
                        validarCedula(cedula);
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtidentificacionKeyReleased

