    private void buscarSueldo() {
        String cargo = cboxcargo.getSelectedItem().toString();
        Cargo car = new Cargo();
        car = cCargo.buscarCargo(cargo);
        txtsueldo.setText(String.valueOf(car.getSueldo()));
    }

    private int validarCampos() {
        int estado = 0;
        if (jcheckjcompleta.isSelected() == false && jcheckjparcial.isSelected() == false) {
            jcheckjcompleta.setForeground(Color.red);
            jcheckjparcial.setForeground(Color.red);
            JOptionPane.showMessageDialog(this, "Debe elegir un tipo de jornada");
        } else {
            jcheckjcompleta.setForeground(Color.black);
            jcheckjparcial.setForeground(Color.black);
            estado++;
        }
        if ((cboxtcontrato.getSelectedItem().toString()) == "Seleccionar") {
            JOptionPane.showMessageDialog(this, "Seleccione un tipo de contrato");
            cboxtcontrato.setForeground(Color.red);
            estado++;
        } else {
            cboxtcontrato.setForeground(Color.black);
        }
        if ((cboxcargo.getSelectedItem().toString()) == "Seleccionar") {
            JOptionPane.showMessageDialog(this, "Seleccione un cargo de trabajo");
            cboxcargo.setForeground(Color.red);
        } else {
            cboxcargo.setForeground(Color.black);
            estado++;
        }
        if ((cboxrelacionLaboral.getSelectedItem().toString()) == "Seleccionar") {
            JOptionPane.showMessageDialog(this, "Seleccione una relacion laboral.");
            cboxrelacionLaboral.setForeground(Color.red);
        } else {
            cboxrelacionLaboral.setForeground(Color.black);
            estado++;
        }
        if (Double.parseDouble(txtsueldo.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un sueldo.");
            txtsueldo.setForeground(Color.red);
        } else {
            txtsueldo.setForeground(Color.black);
            estado++;
        }
        return estado;
    }

    private Contrato obenerDatos() {
        int jornada = 0;
        String fInicio = "";
        String tContrato = "";
        String rLaboral = "";
        String cargo = "";
        double sueldo = 0.00;
        boolean prueba = false;

        Contrato contra = new Contrato();
        if (jcheckjcompleta.isSelected()) {
            jornada = 1;
        }
        if (jcheckjparcial.isSelected()) {
            jornada = 2;
        }
        fInicio = date.getText();
        tContrato = cboxtcontrato.getSelectedItem().toString();
        rLaboral = cboxrelacionLaboral.getSelectedItem().toString();
        cargo = cboxcargo.getSelectedItem().toString();
        sueldo = Double.parseDouble(txtsueldo.getText());
        prueba = jcheckperidoPrueba.isSelected();

        contra.setJornada(jornada);
        contra.setFechaIngreso(fInicio);
        contra.setRelacionLaboral(rLaboral);
        contra.setCargo(cargo);
        contra.setSueldo(sueldo);
        contra.setPeriodoPrueba(prueba);
        contra.setTrabajador(trabajador);
        contra.setEmpresa(empreesa);
        return contra;
    }

