    private void btnjornadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnjornadaActionPerformed
        // TODO add your handling code here:

        String jornada="";
        if (jcheckjcompleta.isSelected()) {
            jornada="Jornada Completa";
        }if (jcheckjparcial.isSelected()) {
            jornada="Jornada Parcial";
        }
        if (jornada!="") {
            VistaHorario vhorario = new VistaHorario(empreesa, jornada);
            VistaInicio.jpaneldesktop.add(vhorario);
            vhorario.toFront();
            vhorario.setVisible(true);
        }else{
            JOptionPane.showConfirmDialog(this,"Primero debe elegir un tipo de jornada");
            jcheckjcompleta.setForeground(Color.red);
            jcheckjparcial.setForeground(Color.red);
        }
        
    }//GEN-LAST:event_btnjornadaActionPerformed

