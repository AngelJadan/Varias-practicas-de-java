    private void txtidentificacionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidentificacionKeyReleased
        // TODO add your handling code here:
        try {
            if (evt.getKeyCode() != 10) {
                String tipoCed = cboxtipoid.getSelectedItem().toString();
                String ident = txtidentificacion.getText();
                if (tipoCed == "Cedula" && ident.length() > 10) {
                    txtidentificacion.setForeground(Color.red);
                    JOptionPane.showMessageDialog(this, "La cedula no debe de exceder los 12 caracteres");
                }
                if (tipoCed == "Cedula" && ident.length() == 10) {
                    validarCedula(txtidentificacion.getText());
                }
            }
        } catch (Exception e) {
        }


    }//GEN-LAST:event_txtidentificacionKeyReleased


