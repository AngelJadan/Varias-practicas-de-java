    private void validarCedula(String cedula) {
        boolean estado = false;
        estado = carga.validaCedula(cedula);
        if (estado == false) {
            txtidentificacion.setForeground(Color.red);
            JOptionPane.showMessageDialog(this, "El numero de cedula es invalido, revise por favor");
        } else if (estado == true) {
            txtidentificacion.setForeground(Color.black);
            buscarTrabajador();
        }
    }

    private void buscarTrabajador() {
        System.out.println("--=-=-=-");
        Trabajador trabajador = new Trabajador();
        trabajador = ct.buscarTrabajador(txtidentificacion.getText(), "", empresa.getCodigo());
        System.out.println(trabajador.getNombre());
        if (trabajador != null) {
            JOptionPane.showMessageDialog(this, "El numero de cedula ingresado es de un trabajador.");
            txtidentificacion.setForeground(Color.red);
        } else {
            txtidentificacion.setForeground(Color.black);
        }
    }

