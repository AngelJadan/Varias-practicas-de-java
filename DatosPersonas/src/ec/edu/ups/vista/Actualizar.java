    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        Persona persona = controladorPersona.buscar(Integer.parseInt(txtCodigo.getText()));
        if (persona != null) {
        txtCedula.setText(persona.getCedula());
        txtNombre.setText(persona.getNombre());
        txtApellido.setText(persona.getApellido());
        
        
        txtTelefono.setText(persona.getTelefono());
        txtADireccion.setText(persona.getDireccion());
        lblImagen.setIcon(persona.getUrlImagen());      
        int edad = persona.getEdad();
        cbxEdad.setSelectedIndex(edad);
        
        if (persona.getGenero() == "Masculino") {
            jrbMasculino.setSelected(true);
        }
        else {
            jrbFemenino.setSelected(true);
        }
            
        }
        
        else {
            JOptionPane.showMessageDialog(this, "PERSONA NO ENCONTRADA");
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarDatosActionPerformed
        // TODO add your handling code here
        Persona persona = controladorPersona.buscar(Integer.parseInt(txtCodigo.getText()));
        persona.setCedula(txtCedula.getText());
        persona.setNombre(txtNombre.getText());
        persona.setApellido(txtApellido.getText());
        persona.setUrlImagen((ImageIcon) lblImagen.getIcon());
        
        String edad = (String)cbxEdad.getSelectedItem();
        persona.setEdad(Integer.parseInt(edad));
        
        persona.setTelefono(txtTelefono.getText());
        persona.setDireccion(txtADireccion.getText());
       
         if (jrbMasculino.isSelected()) {
            persona.setGenero(jrbMasculino.getText());
        } else {
            persona.setGenero(jrbFemenino.getText());
        }
        controladorPersona.actualizar(persona);
        JOptionPane.showMessageDialog(this,"PERSONA ACTUALIZADA ");
        
        
        
    }//GEN-LAST:event_btnActualizarDatosActionPerformed

