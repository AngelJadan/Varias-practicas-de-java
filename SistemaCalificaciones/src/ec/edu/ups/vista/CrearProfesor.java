    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        Profesor profesor = new Profesor();

        profesor.setCodigo(Integer.parseInt(txtcodigo.getText()));
        profesor.setNombre(txtnombre.getText());
        profesor.setApellido(txtapellido.getText());
        profesor.setCedula(txtcedula.getText());
        profesor.setEdad(Integer.parseInt((String) cboxedad.getSelectedItem()));
        profesor.setSueldo(Double.parseDouble(txtsueldo.getText()));
        profesor.setCelular(txtcelular.getText());

        boolean resp = cprofesor.crear(profesor);
        if (resp != false) {
            JOptionPane.showMessageDialog(this, "Datos guardados");
        } else {
            JOptionPane.showMessageDialog(this, "No se ha podido guardar");
        }

    }//GEN-LAST:event_btnguardarActionPerformed

